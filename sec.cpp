#include<iostream>
using namespace std;
// input output methods 
int main(){
    int a , b;
    // << this is called insertion operator 
    cout << "Enter the value integers a \n";
    // >> is called extraction operator
    cin >> a;
    cout << "Enter the value integers b \n";
    cin >> b;
    cout << "the sum of the values of a and b is=> "<< a+b << endl;
    return 0;
}
